/**
 * 获取所有时间轴事件（按时间排序）
 */
export function getAllTimelineEvents() {
  const allEvents = []
  Object.values(timelineEvents).forEach(chapterEvents => {
    allEvents.push(...chapterEvents)
  })
  return allEvents.sort((a, b) => a.year - b.year)
}

/**
 * 根据章节获取事件
 */
export function getEventsByChapter(chapter) {
  return timelineEvents[chapter] || []
}

/**
 * 根据ID获取事件
 */
export function getEventById(id) {
  for (const chapterEvents of Object.values(timelineEvents)) {
    const event = chapterEvents.find(e => e.id === id)
    if (event) return event
  }
  return null
}

/**
 * 检查事件是否已解锁
 */
export function isEventUnlocked(eventId, unlockedLevels) {
  const event = getEventById(eventId)
  if (!event) return false
  return unlockedLevels.includes(event.unlockLevel)
}

/**
 * 获取解锁进度
 */
export function getUnlockProgress(unlockedLevels) {
  const allEvents = getAllTimelineEvents()
  const unlockedCount = allEvents.filter(e => 
    unlockedLevels.includes(e.unlockLevel)
  ).length
  return {
    total: allEvents.length,
    unlocked: unlockedCount,
    percentage: Math.round((unlockedCount / allEvents.length) * 100)
  }
}
